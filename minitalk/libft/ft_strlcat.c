#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t n)
{
	size_t	i;
	size_t	j;

	if (n <= ft_strlen(dst))
		return (n + ft_strlen(src));
	i = 0;
	j = ft_strlen(dst);
	while (src[i] != '\0' && j < (n - 1))
	{
		dst[j] = src[i];
		i++;
		j++;
	}
	dst[j] = '\0';
	return (ft_strlen(dst) + ft_strlen(&src[i]));
}
