#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	sg;
	int	n;

	sg = 1;
	n = 0;
	i = 0;
	while (((str[i] >= 9 && str[i] <= 13) || str[i] == 32) && str[i])
		i++;
	if (str[i] == '-')
	{
		sg = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while ((str[i] >= '0' && str[i] <= '9'))
	{
		n = n * 10 + (str[i] - 48);
		i++;
	}
	return (n * sg);
}
